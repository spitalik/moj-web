// ==================== Language Manager ====================
class LanguageManager {
    constructor() {
        this.currentLang = this.getSavedLanguage();
        this.init();
    }

    getSavedLanguage() {
        try {
            const saved = localStorage.getItem('gamehub_lang');
            if (saved && translations[saved]) return saved;
        } catch(e) {}
        return 'en';
    }

    saveLanguage(lang) {
        try {
            localStorage.setItem('gamehub_lang', lang);
        } catch(e) {}
        this.currentLang = lang;
    }

    init() {
        document.documentElement.lang = this.currentLang;
        this.translatePage();
    }

    translate(key, lang = this.currentLang) {
        return translations[lang]?.[key] || translations['en'][key] || key;
    }

    translatePage() {
        const elements = document.querySelectorAll('[data-translate]');
        elements.forEach(el => {
            const key = el.getAttribute('data-translate');
            if (el.placeholder !== undefined && el.tagName === 'INPUT') {
                el.placeholder = this.translate(key);
            } else {
                el.textContent = this.translate(key);
            }
        });
        
        // Update language button display
        const langBtn = document.querySelector('#langToggle .current-lang');
        if (langBtn) langBtn.textContent = this.currentLang.toUpperCase();
        
        // Trigger custom event for games to listen to
        document.dispatchEvent(new CustomEvent('languageChanged', { 
            detail: { lang: this.currentLang } 
        }));
    }

    setLanguage(lang) {
        if (!translations[lang]) return;
        this.saveLanguage(lang);
        this.currentLang = lang;
        document.documentElement.lang = lang;
        this.translatePage();
        
        // Update dropdown active state
        document.querySelectorAll('.lang-option').forEach(opt => {
            opt.classList.toggle('active', opt.getAttribute('data-lang') === lang);
        });
        
        // If game is loaded in iframe, send language change
        this.notifyGameFrame(lang);
    }

    notifyGameFrame(lang) {
        // Find all game iframes and post message
        const iframes = document.querySelectorAll('iframe[data-game-frame]');
        iframes.forEach(iframe => {
            try {
                iframe.contentWindow.postMessage({ 
                    type: 'languageChange', 
                    lang: lang 
                }, '*');
            } catch(e) {}
        });
    }
}

